    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if(Common.isEmptyString(username.getText()) || Common.isEmptyString(password.getText()))
        {
            JOptionPane.showMessageDialog(null, "Please fill all the fields.");
        } else {
            logedInUser.setUserName(username.getText());
            logedInUser.setPassword(password.getText());
            if(logedInUser.login()){
                this.setVisible(false);
                if(logedInUser.getRole().equalsIgnoreCase("ADMIN")) {
                   new AdminHome().setVisible(true);
                } else {
                    new UserHome().setVisible(true);
                }
            } else {
                JOptionPane.showMessageDialog(null, "Invalid Username or Password.");
            }
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_jButton2ActionPerformed

