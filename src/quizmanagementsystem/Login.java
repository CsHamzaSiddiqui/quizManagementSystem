    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (Common.isEmptyString(name.getText()) || Common.isEmptyString(email.getText()) || Common.isEmptyString(username1.getText()) || Common.isEmptyString(password1.getText())) {
            JOptionPane.showMessageDialog(null, "Please fill all fields for registration.");
        } else {
            logedInUser.setId(Common.generatePrimaryKey());
            logedInUser.setName(name.getText());
            logedInUser.setEmail(email.getText());
            logedInUser.setPassword(password1.getText());
            logedInUser.setUserName(username1.getText());
            logedInUser.setRole("USER");
            logedInUser.setDeleted(false);
            logedInUser.save();
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_jButton1ActionPerformed

